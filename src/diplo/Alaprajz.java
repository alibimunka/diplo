    public class termek {
        private int elerheto;

        public int getElerheto() {
            return elerheto;
        }

        public void setElerheto(int elerheto) {
            this.elerheto = elerheto;
        }

        public String getNev() {
            return nev;
        }

        public void setNev(String nev) {
            this.nev = nev;
        }
        private String nev;
        termek(String szoveg, int szam){
            this.elerheto = szam;
            this.nev=szoveg;
        }
        termek(){
            this.elerheto = 0;
            this.nev = "nincs adat";
        }
    }
    
