    private void HatterLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_HatterLabelMouseClicked
        // TODO add your handling code here:
        int xcor = MouseInfo.getPointerInfo().getLocation().x - HatterLabel.getLocationOnScreen().x;
        int ycor = MouseInfo.getPointerInfo().getLocation().y - HatterLabel.getLocationOnScreen().y;
        x = xcor/10;
        y = ycor/10;
        System.out.println(x*10+":"+y*10);
        switch (mode) {
            case 1:  {
                adatok[x][y] = new termek(BeTextField.getText(),1);
                break;
            }
            case 2:  {
                if (adatok[x][y].getElerheto()==1) KiTextField.setText(adatok[x][y].getNev());
                else KiTextField.setText("Nincs adat");
                break;
            }
            default: System.out.println("Not a month!"); break;
        }
    }//GEN-LAST:event_HatterLabelMouseClicked

